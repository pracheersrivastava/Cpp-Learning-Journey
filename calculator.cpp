#include <iostream>
using namespace std;

int main() {
    int a;
    int b;
    cout<<"Enter first number: ";
    cin>>a;
    cout<<"Enter second number: ";
    cin>>b;
    cout<<"Sum is: "<<(a+b)<<endl;
    cout<<"Difference is: "<<(a-b)<<endl;
    cout<<"Product is: "<<(a*b)<<endl;
    cout<<"Quotient is: "<<(a/b)<<" and Remainder is: "<<(a%b)<<endl;
return 0;
}